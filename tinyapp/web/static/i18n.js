// ── System ──

function _detectLang() {
  var bl = (navigator.language || 'en').slice(0, 2);
  return i18n[bl] ? bl : 'en';
}

var _lang = localStorage.getItem('cove-lang') || _detectLang();

function t(key) {
  return (i18n[_lang] && i18n[_lang][key]) || i18n.en[key] || key;
}

function currentLang() { return _lang; }

function setLang(code) {
  if (!i18n[code]) return;
  _lang = code;
  localStorage.setItem('cove-lang', code);
  window.dispatchEvent(new Event('langchange'));
}

// Apply translations to all [data-i18n], [data-i18n-html], [data-i18n-placeholder] elements
function applyI18n() {
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    el.textContent = t(el.getAttribute('data-i18n'));
  });
  document.querySelectorAll('[data-i18n-html]').forEach(function(el) {
    el.innerHTML = t(el.getAttribute('data-i18n-html'));
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el) {
    el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
  });
  document.documentElement.lang = currentLang();
  var sel = document.getElementById('lang-switcher');
  if (sel) sel.value = currentLang();
}

// Init language switcher + listen for changes
function initLangSwitcher() {
  var sel = document.getElementById('lang-switcher');
  if (!sel) return;
  sel.innerHTML = SUPPORTED_LANGS.map(function(l) {
    return '<option value="' + l.code + '">' + l.name + '</option>';
  }).join('');
  sel.value = currentLang();
  sel.addEventListener('change', function() { setLang(this.value); });
  window.addEventListener('langchange', applyI18n);
}

